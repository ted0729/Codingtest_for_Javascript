function solution(left, right) {
    var answer = 0;

    for(let i=left; i<=right; i++) {
        Math.sqrt(i) % 1 === 0 ? answer-=i : answer+=i;
    } // 제곱근이 정수일때는 빼줘라 !
      // 9, 16, 25, 36, 49, 64 ....
      // 제곱수는 약수의 개수가 홀수이다..
    return answer;
}

